package Day35;

import java.util.Scanner;

public class Main {
    
    public static void main(String[] args) {
        
        Scanner scanner = new Scanner(System.in);
        int batas;

        System.out.print("Masukkan Batas angka : ");
        batas = scanner.nextInt();

        // Perulangan
        for (int i = 1; i <= batas; i++) {
            
            // Menampilkan angka
            System.out.println(i);
    }
    
  }
}
